    public void load_text_fields() {
        String line;
        try {
            base64.decode_from_base64();
            File temp_config_file = new File(fw.getDefaultDirectory() + "\\Library\\config_temp.dat");
            FileReader fr = new FileReader(temp_config_file);
            BufferedReader br = null;
            if(temp_config_file.exists()){
                br = new BufferedReader(fr);
            }
            
            while ((line = br.readLine()) != null) {
                String mysql_uname = br.readLine();
                String mysql_pass = br.readLine();
                String mysql_server = br.readLine();
                String mysql_db = br.readLine();
                config_uname_txt.setText(mysql_uname);
                config_pass_txt.setText(mysql_pass);
                config_server_txt.setText(mysql_server);
                config_db_txt.setText(mysql_db);
            }
            //fetching details from the temporary config file and delete it
            fr.close();
            br.close();
            temp_config_file.delete();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error reading the config file!");
        }
    }


