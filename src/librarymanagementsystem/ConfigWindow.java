    private void config_save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_config_save_btnActionPerformed

        if (!config_server_txt.getText().trim().equals("") && !config_uname_txt.getText().trim().equals("") && !config_db_txt.getText().trim().equals("")) {
            File file = new File("config.dat");

            File library_ms_dir = new File(fw.getDefaultDirectory() + "\\Library");

            if (!library_ms_dir.exists()) {
                try {
                    library_ms_dir.mkdir();
                } catch (SecurityException ex) {
                    JOptionPane.showMessageDialog(null, "Cannot create a folder in your document, because of security of your system");
                }
            }

            try {
                FileOutputStream writeStream = new FileOutputStream(file, false); // true to append
                PrintWriter pw = new PrintWriter(writeStream);
                pw.println("");
                pw.println(config_uname_txt.getText().trim());
                pw.println(config_pass_txt.getText().trim());
                pw.println(config_server_txt.getText().trim());
                pw.println(config_db_txt.getText().trim());
                pw.flush();
                pw.close();

                base64.encode_to_base64(); //convert temporary file to Base64
                file.delete(); //delete temporary created file
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error writting to config file!");
            }

            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Server name, Username and Database fields are required!");
        }

    }//GEN-LAST:event_config_save_btnActionPerformed

